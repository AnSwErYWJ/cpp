#include<iostream>

using namespace std;

int main(void)
{
    std::cout<<"The size of short is :\t\t";
    std::cout<<sizeof(short)<<"bytes\n";
    std::cout<<"The size of int is :\t\t";
    std::cout<<sizeof(int)<<"bytes\n";
    std::cout<<"The size of long is :\t\t";
    std::cout<<sizeof(long)<<"bytes\n";
    std::cout<<"The size of long long int is :\t\t";
    std::cout<<sizeof(long long int)<<"bytes\n";
    std::cout<<"The size of char is :\t\t";
    std::cout<<sizeof(char)<<"bytes\n";
    std::cout<<"The size of bool is :\t\t";
    std::cout<<sizeof(bool)<<"bytes\n";
    std::cout<<"The size of float is :\t\t";
    std::cout<<sizeof(float)<<"bytes\n";
    std::cout<<"The size of double is :\t\t";
    std::cout<<sizeof(double)<<"bytes\n";

    return 0;
}
